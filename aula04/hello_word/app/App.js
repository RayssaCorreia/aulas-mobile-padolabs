import React, { Component } from 'react';
import { Button, View, Text, StyleSheet } from 'react-native';

class Home extends Component {
  constructor(props) {
    super(props);

    this.state = {
      counter: 0,
      nome: 'teste'
    };
  }



  componentWillMount() {}

  render() {
    const { counter } = this.state;
    return (
      <View style={styles.container}>
        {/* <Text style={styles.title}> Contador: {this.state.counter}</Text> */}
        <Text style={styles.title}> Contador: {counter}</Text>
        <Button title="Incrementar" style={styles.button} onPress={() => this.setState({counter: this.state.counter + 1})}></Button>
      </View>  
    );
  }
  
}


const styles = StyleSheet.create({
  container: {
    flex: 1,
    padding: 40,
  },
  title: {
    marginTop: 8,
    marginBottom: 8,
    paddingVertical: 8,
    borderWidth: 4,
    borderColor: "#20232a",
    borderRadius: 6,
    backgroundColor: "#61dafb",
    color: "#20232a",
    textAlign: "center",
    fontSize: 30,
    fontWeight: "bold"
  },
  button:{
    marginLeft: 40,
    marginRight: 40,
  }
});


export default Home;

